package com.benharvey.imagesearch.helpers;

public class GoogleImage {
	String thumbUrl;
	String title;
	
	public String getThumbUrl() 
	{
		return thumbUrl;
	}
	
	public void setThumbUrl(String url) 
	{
		this.thumbUrl = url;
	}
	
	public String getTitle() 
	{
		return title;
	}
	
	public void setTitle(String title) 
	{
		this.title = title;
	}
}
